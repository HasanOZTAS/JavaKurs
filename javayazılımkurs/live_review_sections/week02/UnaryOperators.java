package week02;

public class UnaryOperators {

	public static void main(String[] args) {
		
		System.out.println(-10 - 3);
		System.out.println(10 - 3);
		
		
		System.out.println("*******");
		int a=10;
		int b=a--;
		
		System.out.println(a+ "   " +b);
		
		System.out.println("*******");
		
		int x=20;
		System.out.println(x++);
		System.out.println(x);
		
		System.out.println("*******");
		
		int y=15;
		System.out.println(y--);
		System.out.println(y);
		
		
						

	}

}

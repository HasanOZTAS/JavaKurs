package day10_controlFlowStatements_Part4;

public class task46_MultiplicationTable {

	public static void main(String[] args) {
		
		
		int x=17;
				
		System.out.println("Multiplicaton Table for "+ x);
		System.out.println("------------------------");
		
		for (int i=1; i<=10; i++) {
			System.out.println(x + " * " + i +" = " + (x*i));
		}
		
		

	}

}

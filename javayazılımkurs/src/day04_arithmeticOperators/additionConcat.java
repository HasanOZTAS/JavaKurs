package day04_arithmeticOperators;

public class additionConcat {

	public static void main(String[] args) {
		
		//Number + Number = Addition
		System.out.println(2+2);
		System.out.println(100+200+50);
		
		System.out.println();
		
		//Number+String : Concatenation
		System.out.println(5 +" Hello");
		
		//String+Boolean : Concatenation
		System.out.println("Hello "+ true);
		
		//String + String : Concatenation
		System.out.println("Hello " + "World");
		
		System.out.println();
		
		//Boolean + Number
		//System.out.println(true+1);  error
		
		System.out.println(2+0+3+ "Cybertek");
		System.out.println("Cybertek" +2+0+5);
		System.out.println(2+0+5+"Cybertek"+2+0+5);
		System.out.println(2+0+5+ "Cybertek" +(2+0+1));
		
		System.out.println(2+(6+5)+ "Cybertek" +2+(5+1));
		
		
		
		
		
		

	}

}
